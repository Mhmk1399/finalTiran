// Helper function to get all cities for a specific province
export const getCitiesByProvinceId = (provinceId: string): City | null => {
  if (provincesWithCities[provinceId]) {
    return provincesWithCities[provinceId].cities;
  }
  return null;
};

// Helper function to get province name by ID
export const getProvinceNameById = (provinceId: string): string | null => {
  return provinces[provinceId] || null;
};

// Helper function to get city name by province ID and city ID
export const getCityNameByIds = (
  provinceId: string,
  cityId: string
): string | null => {
  const province = provincesWithCities[provinceId];
  if (province && province.cities[cityId]) {
    return province.cities[cityId];
  }
  return null;
};

// Get all provinces as an array of objects for dropdown lists
export const getProvincesForDropdown = (): Array<{
  id: string;
  name: string;
}> => {
  return Object.entries(provinces).map(([id, name]) => ({
    id,
    name,
  }));
};

// Get all cities of a province as an array of objects for dropdown lists
export const getCitiesForDropdown = (
  provinceId: string
): Array<{ id: string; name: string }> => {
  const province = provincesWithCities[provinceId];
  if (!province) return [];

  return Object.entries(province.cities).map(([id, name]) => ({
    id,
    name,
  }));
};
